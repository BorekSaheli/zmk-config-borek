void draw_custom_art(lv_obj_t *canvas, lv_color_t color) {
    lv_draw_rect_dsc_t rect_dsc;
    init_rect_dsc(&rect_dsc, color);

    const uint8_t *pixels = art_data + PALETTE_BYTES;

    for (int row = 0; row < ART_H; row++) {
        const uint8_t *row_data = pixels + row * ROW_BYTES;
        for (int col = 0; col < ART_W; col++) {
            int byte_idx = col / 8;
            int bit_idx = 7 - (col % 8);
            if (!(row_data[byte_idx] & (1 << bit_idx))) {
                canvas_draw_rect(canvas, col, row, 1, 1, &rect_dsc);
            }
        }
    }
}